const arr = [12,32,[42,32,[32,4,[32,43,23,100,[23,1000]]]]];

function FlatenArray(arr){
    const flatenArray = arr.reduce((acc,cur)=>{
        return acc.concat(Array.isArray(cur) ? FlatenArray(cur) : cur)
    },[])
    return flatenArray;
}
console.log(FlatenArray(arr))

